function sum(n1, n2) {
    return n1 + n2;
}

function substract(n, m) {
    return n - m;
}

let n1 = 23;
let n2 = 6;
let n3 = 10;

console.log(substract(sum(n1, n2), n3));

n1 = 1;
n2 = 17;
n3 = 30;

console.log(substract(sum(n1, n2), n3));

